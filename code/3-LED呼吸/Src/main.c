int All_Time=40000000;//单次呼吸时间
int LED_TIME=20000;//每次亮灭周期

void Led(uint32_t led_time,uint32_t piecetime)
{
		HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_13);//LED电平自动翻转
		My_Delay(piecetime);
		HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_13);//LED电平自动翻转
		My_Delay(led_time-piecetime);
}
void My_Delay(uint32_t delay_time)
{
		uint32_t count;
		HAL_TIM_Base_Stop_IT(&htim2);
		count=0;
		HAL_TIM_Base_Start_IT(&htim2);
		while(count <= delay_time){
			count++;
		}
		HAL_TIM_Base_Stop_IT(&htim2);
}
